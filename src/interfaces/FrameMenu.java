    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // botón actualizar
        // Verifica si el campo de código está vacío
        if ("".equals(txtCodigo.getText())) {
            JOptionPane.showMessageDialog(null, "Seleccione una fila:");
        } else {
            // Verifica que todos los campos estén llenos
            if (!"".equals(txtCodigo.getText()) && !"".equals(txtNombre.getText()) && !"".equals(txtCantidad.getText()) && !"".equals(txtPrecio.getText()) && !"".equals(txtCategoria.getText()) && !"".equals(txtProveedor.getText())) {

                // Si todos los campos tienen datos, entonces actualizamos el producto
                ni.setId_codigo(Integer.parseInt(txtCodigo.getText()));
                ni.setNombre(txtNombre.getText());
                ni.setCantidad(Integer.parseInt(txtCantidad.getText()));
                ni.setPrecio(Double.parseDouble(txtPrecio.getText()));
                ni.setCategoria(txtCategoria.getText());
                ni.setId_proveedor(Integer.parseInt(txtProveedor.getText()));
                // Llamamos al método para modificar el producto
                nid.modificarProducto(ni);
                // Limpiamos la tabla, los campos de texto y volvemos a listar los productos
                LimpiarTable();
                limpiarProducto();
                listarProducto();
            } else {
                // Si algún campo está vacío, mostramos un mensaje de advertencia
                JOptionPane.showMessageDialog(null, "Campos vacíos");
            }
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

