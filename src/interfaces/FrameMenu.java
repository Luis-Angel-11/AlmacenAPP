    public void listarProducto() {
        List<nuevoIngreso> lispro = nid.listarProducto();
        modelo = (DefaultTableModel) TableNuevoIngreso.getModel();
        Object[] ob = new Object[6];
        for (int i = 0; i < lispro.size(); i++) {
            ob[0] = lispro.get(i).getId_codigo();
            ob[1] = lispro.get(i).getNombre();
            ob[2] = lispro.get(i).getCantidad();
            ob[3] = lispro.get(i).getPrecio();
            ob[4] = lispro.get(i).getCategoria();
            ob[5] = lispro.get(i).getId_proveedor();
            modelo.addRow(ob);

        }

        TableNuevoIngreso.setModel(modelo);
    }

    public void listarProveedor() {
        List<proveedor> lisproveedor = pd.listarProveedor();
        modelo = (DefaultTableModel) TableProveedor.getModel();
        Object[] ob = new Object[5];
        for (int i = 0; i < lisproveedor.size(); i++) {
            ob[0] = lisproveedor.get(i).getId_proveedor();
            ob[1] = lisproveedor.get(i).getNombre();
            ob[2] = lisproveedor.get(i).getUbicacion();
            ob[3] = lisproveedor.get(i).getTelefono();
            ob[4] = lisproveedor.get(i).getCorreo();
            modelo.addRow(ob);

        }

        TableProveedor.setModel(modelo);
    }

    public void LimpiarTable() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }

