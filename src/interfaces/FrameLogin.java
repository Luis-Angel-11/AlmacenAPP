    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        //boton de ingreso
        String usuario, contrasenia;
        usuario = txtUsuario.getText();
        contrasenia = String.valueOf(txtContrasenia.getPassword());
        
        String query= "SELECT * FROM administradores WHERE usuario='"+usuario+"' and contrasenia='"+contrasenia+"'";

        try {
            Statement st = cx.conectar().createStatement();
            ResultSet rs = st.executeQuery(query);
            //si existe en la base de datos 
            if(rs.next()){
                JOptionPane.showMessageDialog(this,"Bienvenido ad. "+ usuario );
                FrameMenu framemenu = new FrameMenu();
                framemenu.setVisible(true);
                this.setVisible(false);
            }else{
                JOptionPane.showMessageDialog(this, "Error, no existe");
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrameLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
       
        
        
        
        
        
        
        
    }//GEN-LAST:event_btnIngresarActionPerformed

